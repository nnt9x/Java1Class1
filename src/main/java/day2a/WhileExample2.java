package day2a;

public class WhileExample2 {
    public static void main(String[] args) {

        /*
        In ra cac so tu 1-> 10 sử dụng while
         */
        // i = 1;  DK: i<=10

        int i = 1;
        while (i <= 10) {
            System.out.print(i + "\t");
            i = i + 1;
        }

    }
}
